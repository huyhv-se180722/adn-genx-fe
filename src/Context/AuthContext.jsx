import React, { createContext, useState, useContext, useEffect } from 'react';
import { jwtDecode } from 'jwt-decode';
import axiosClient from '../config/AxiosClient';
import { refreshAccessToken } from '../Components/Service/refreshAccessToken';

export const AuthContext = createContext(null);

export const AuthProvider = ({ children }) => {
  const [isLoggedIn, setIsLoggedIn] = useState(false);
  const [user, setUser] = useState(null);
  const [role, setRole] = useState(null);
  const [loading, setLoading] = useState(true);

  // Validate token v·ªõi jwt-decode
  const validateToken = (token) => {
    try {
      const decoded = jwtDecode(token);
      const currentTime = Date.now() / 1000;
      
      if (decoded.exp && decoded.exp > currentTime) {
        return decoded;
      } else {
        console.log('Token expired:', new Date(decoded.exp * 1000));
        return null;
      }
    } catch (error) {
      console.error('Invalid token:', error);
      return null;
    }
  };

  // ‚úÖ H√†m refresh token t·ª´ cookie
const refreshFromCookie = async () => {
  try {
    console.log('üîÑ Attempting to refresh token from cookie...');
    console.log("üåç API URL = ", import.meta.env.VITE_API_BASE_URL);

    // G·ªçi API ƒë·ªÉ l·∫•y accessToken m·ªõi t·ª´ cookie ch·ª©a refreshToken
    const newAccessToken = await refreshAccessToken(); // h√†m ƒë√£ vi·∫øt s·∫µn

    if (newAccessToken) {
      // ‚úÖ Gi·∫£i m√£ access token (ch·ªâ ƒë·ªÉ ki·ªÉm tra th·ªùi h·∫°n, sub, role, v.v.)
      const decoded = jwtDecode(newAccessToken);
      const expiry = decoded.exp ? new Date(decoded.exp * 1000) : null;
      console.log("‚è∞ Token expires at:", expiry);

      // ‚úÖ L·∫•y th√¥ng tin user t·ª´ server
      const response = await axiosClient.get('/api/v1/account/profile');
      const userData = response.data;

      if (userData) {
        setIsLoggedIn(true);
        setUser(userData);
        // getUser(userData);
        setRole(userData.role);
        localStorage.setItem('accessToken', newAccessToken);
        localStorage.setItem('user', JSON.stringify(userData));

        console.log('‚úÖ Token refreshed successfully, user restored:', userData);
        return true;
      }
    }

    return false;
  } catch (error) {
    console.error('‚ùå Refresh from cookie failed:', error);
    return false;
  }
};


  // ‚úÖ LOGIC HO√ÄN TO√ÄN M·ªöI - x·ª≠ l√Ω t·∫•t c·∫£ tr∆∞·ªùng h·ª£p
  useEffect(() => {
    const checkAuth = async () => {
      try {
        const token = localStorage.getItem('accessToken');
        const storedUser = localStorage.getItem('user');

        // ‚úÖ Tr∆∞·ªùng h·ª£p 1: C√≥ token v√† user trong localStorage
        if (token && storedUser) {
          const decodedToken = validateToken(token);
          
          if (decodedToken) {
            // Token c√≤n h·∫°n, kh√¥i ph·ª•c user
            try {
              const userData = JSON.parse(storedUser);
              setIsLoggedIn(true);
              setUser(userData);
              setRole(userData.role);
              console.log('‚úÖ User restored from localStorage:', userData.username);
            } catch (parseError) {
              console.error('‚ùå Error parsing stored user:', parseError);
              await handleInvalidToken();
            }
          } else {
            // Token h·∫øt h·∫°n, th·ª≠ refresh
            console.log('‚è∞ Token expired, attempting refresh...');
            const refreshSuccess = await refreshFromCookie();
            
            if (!refreshSuccess) {
              await handleInvalidToken();
            }
          }
        }
        // ‚úÖ Tr∆∞·ªùng h·ª£p 2: Kh√¥ng c√≥ token (F5, reload) nh∆∞ng c√≥ th·ªÉ c√≥ refreshToken
        else {
          console.log('üîç No token in localStorage, checking cookie...');
          const refreshSuccess = await refreshFromCookie();
          
          if (!refreshSuccess) {
            // Kh√¥ng c√≥ g√¨ c·∫£ ho·∫∑c refresh fail
            console.log('üë§ No valid session found');
            setIsLoggedIn(false);
            setUser(null);
            setRole(null);
          }
        }
      } catch (error) {
        console.error('‚ùå Auth check error:', error);
        await handleInvalidToken();
      } finally {
        setLoading(false);
      }
    };

    checkAuth();
  }, []);

  // X·ª≠ l√Ω token kh√¥ng h·ª£p l·ªá
  const handleInvalidToken = async () => {
    console.log('üßπ Cleaning up invalid session...');
    setIsLoggedIn(false);
    setUser(null);
    setRole(null);
    localStorage.removeItem('accessToken');
    localStorage.removeItem('user');
    localStorage.removeItem('redirectUrl');
  };

  // Check token expiry
  const checkTokenExpiry = () => {
    const token = localStorage.getItem('accessToken');
    if (!token) return false;
    
    const decodedToken = validateToken(token);
    return !!decodedToken;
  };

  // Get token expiry time
  const getTokenExpiryTime = () => {
    const token = localStorage.getItem('accessToken');
    if (!token) return null;
    
    try {
      const decoded = jwtDecode(token);
      return decoded.exp ? new Date(decoded.exp * 1000) : null;
    } catch (error) {
      return null;
    }
  };

  // Login function
  const login = (userData) => {
    setIsLoggedIn(true);
    setUser(userData);
    setRole(userData.role);
    localStorage.setItem('accessToken', userData.accessToken);
    localStorage.setItem('user', JSON.stringify(userData));
    
    console.log('‚úÖ User logged in:', userData.username);
    
    const expiryTime = getTokenExpiryTime();
    if (expiryTime) {
      console.log('‚è∞ Token expires at:', expiryTime);
    }
  };

  // Logout function
  const logout = async () => {
    try {
      console.log("üîÑ Calling logout API...");
      await axiosClient.post("/api/v1/auth/logout");
      console.log("‚úÖ Logout API success");
    } catch (err) {
      console.error("‚ùå Logout API error:", err);
    }
    
    setIsLoggedIn(false);
    setUser(null);
    setRole(null);
    localStorage.removeItem('accessToken');
    localStorage.removeItem('user');
    localStorage.removeItem('redirectUrl');
    
    console.log("üîÅ Redirecting to home...");
    window.location.href = '/';
  };

  const checkAuthentication = () => {
    return isLoggedIn && user !== null && checkTokenExpiry();
  };

  const checkLogin = (redirectPath) => {
    if (!checkAuthentication()) {
      localStorage.setItem('redirectUrl', redirectPath);
      return false;
    }
    return true;
  };

  if (loading) {
    return (
      <div style={{
        display: 'flex',
        justifyContent: 'center',
        alignItems: 'center',
        height: '100vh',
        fontSize: '18px'
      }}>
        ƒêang ki·ªÉm tra ƒëƒÉng nh·∫≠p...
      </div>
    );
  }

  return (
    <AuthContext.Provider
      value={{
        isLoggedIn,
        user,
        role,
        loading,
        login,
        logout,
        checkLogin,
        checkAuthentication,
        checkTokenExpiry,
        getTokenExpiryTime,
        handleInvalidToken
      }}
    >
      {children}
    </AuthContext.Provider>
  );
};

export const useAuth = () => {
  const context = useContext(AuthContext);
  if (!context) {
    throw new Error('useAuth must be used within an AuthProvider');
  }
  return context;
};